const canvas = document.querySelector("#fireworksCanvas");
const ctx = canvas.getContext("2d");
const envelopeGrid = document.querySelector("#envelopeGrid");
const letterDialog = document.querySelector("#letterDialog");
const closeDialog = document.querySelector("#closeDialog");
const dialogSender = document.querySelector("#dialogSender");
const dialogTitle = document.querySelector("#dialogTitle");
const dialogMessage = document.querySelector("#dialogMessage");
const FIREWORK_INTERVAL_MS = 20000;

let particles = [];
let animationFrame = 0;

function renderEnvelopes() {
  envelopeGrid.innerHTML = "";

  letters.forEach((letter, index) => {
    const button = document.createElement("button");
    button.className = "envelope";
    button.type = "button";
    button.setAttribute("aria-label", `${letter.sender}의 편지 열기`);
    button.innerHTML = `
      <strong>From. ${escapeHtml(letter.sender)}</strong>
    `;
    button.addEventListener("click", () => openLetter(index));
    envelopeGrid.appendChild(button);
  });
}

function openLetter(index) {
  const letter = letters[index];
  dialogSender.textContent = `From. ${letter.sender}`;
  dialogTitle.textContent = `${letter.sender}의 편지`;
  dialogMessage.textContent = letter.message;

  if (typeof letterDialog.showModal === "function") {
    letterDialog.showModal();
  } else {
    letterDialog.setAttribute("open", "");
  }
}

function escapeHtml(value) {
  return value.replace(/[&<>"']/g, (match) => {
    const entities = {
      "&": "&amp;",
      "<": "&lt;",
      ">": "&gt;",
      '"': "&quot;",
      "'": "&#039;",
    };
    return entities[match];
  });
}

closeDialog?.addEventListener("click", () => {
  letterDialog?.close();
});

letterDialog?.addEventListener("click", (event) => {
  const dialogBox = letterDialog.querySelector(".letter-card");
  if (dialogBox && !dialogBox.contains(event.target)) {
    letterDialog.close();
  }
});

function resizeCanvas() {
  const ratio = Math.min(window.devicePixelRatio || 1, 2);
  canvas.width = Math.floor(window.innerWidth * ratio);
  canvas.height = Math.floor(window.innerHeight * ratio);
  canvas.style.width = `${window.innerWidth}px`;
  canvas.style.height = `${window.innerHeight}px`;
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);
}

function launchFirework(x, y, count = 70) {
  const colors = ["#ff6b5f", "#30c7a4", "#f5b84b", "#ffffff", "#7da2ff"];

  for (let i = 0; i < count; i += 1) {
    const angle = (Math.PI * 2 * i) / count + Math.random() * 0.24;
    const speed = 2.4 + Math.random() * 4.6;
    particles.push({
      x,
      y,
      vx: Math.cos(angle) * speed,
      vy: Math.sin(angle) * speed,
      alpha: 1,
      size: 1.7 + Math.random() * 2.7,
      color: colors[Math.floor(Math.random() * colors.length)],
      gravity: 0.035 + Math.random() * 0.025,
      decay: 0.011 + Math.random() * 0.012,
    });
  }

  if (!animationFrame) {
    animateFireworks();
  }
}

function launchShow() {
  const width = window.innerWidth;
  const height = window.innerHeight;
  const points = [
    [width * 0.2, height * 0.22],
    [width * 0.5, height * 0.16],
    [width * 0.78, height * 0.25],
    [width * 0.36, height * 0.34],
    [width * 0.64, height * 0.36],
  ];

  points.forEach(([x, y], index) => {
    window.setTimeout(() => launchFirework(x, y, 72), index * 180);
  });
}

function animateFireworks() {
  ctx.clearRect(0, 0, window.innerWidth, window.innerHeight);

  particles = particles.filter((particle) => particle.alpha > 0.02);
  particles.forEach((particle) => {
    particle.x += particle.vx;
    particle.y += particle.vy;
    particle.vy += particle.gravity;
    particle.vx *= 0.99;
    particle.alpha -= particle.decay;

    ctx.globalAlpha = Math.max(particle.alpha, 0);
    ctx.fillStyle = particle.color;
    ctx.beginPath();
    ctx.arc(particle.x, particle.y, particle.size, 0, Math.PI * 2);
    ctx.fill();
  });

  ctx.globalAlpha = 1;

  if (particles.length) {
    animationFrame = requestAnimationFrame(animateFireworks);
  } else {
    animationFrame = 0;
    ctx.clearRect(0, 0, window.innerWidth, window.innerHeight);
  }
}

window.addEventListener("resize", resizeCanvas);
resizeCanvas();
renderEnvelopes();

window.setTimeout(launchShow, 500);
window.setInterval(launchShow, FIREWORK_INTERVAL_MS);
